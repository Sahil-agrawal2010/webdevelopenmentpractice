# Modules :- It is the method of using code developed by other developers.Modules are well tested, less error, used by many people etc.
# Modules are like a code library which can be used to borrow a code written by somebody else.
# There are two types of modules in Python :- 
# 1) Built in Modules :- These modules are attached in python,We can access it after downloading and installing python.
# 2)External Modules :- These are the type of modules which are not attached in pytho,built by other developers, We need to use pip command to access those modules

# PIP Command :- PIP is a pakage manager which stands for PREFERED INSTALLER PROGRAMME.This command is used to install various python modules.


# Examples:-
#           pip install pandas :- This will install a module named pandas. However it is the wrond and dangerous way to use pip command
